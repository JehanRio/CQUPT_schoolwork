#include<iostream>
#include<string>
#include<queue>
using namespace std;
char map[35][55];
bool vis[35][55];
int dir[4][2]={{1,0},{0,-1},{0,1},{-1,0}};//D<L<R<U
char ch[4]={'D','L','R','U'};
#define N 30
#define M 50
struct point
{
    int x;
    int y;
    string road;
    point(int xx,int yy,string s){x=xx;y=yy;road=s;}
    point(int xx,int yy){x=xx;y=yy;}
};
void bfs()
{
    queue<point> t;
    point p(1,1,"");//起点初始化
    t.push(p);
    vis[1][1]=1;//一开始的结点访问过了
    while(!t.empty())
    {
        point q=t.front();
        t.pop();
        if(q.x==30 && q.y==50)
        {
            cout<<q.road;
            return;
        }
        for(int i=0;i<4;i++)
        {
            int dx=q.x+dir[i][0],dy=q.y+dir[i][1];
            if(dx>=1 && dx<=50 && dy>=0 && dy<=50)
            {
                if(map[dx][dy]=='0' && !vis[dx][dy])
                {
                    point tt(dx,dy,q.road+ch[i]);
                    t.push(tt);
                    vis[dx][dy]=1;//BFS 找到的路径一定是最短的 所以直接vis=1
                }
            }
        }
    }
}
int main()
{
    for(int i=1;i<=30;i++)
    {
        for(int j=1;j<=50;j++)
        cin>>map[i][j];
        //scanf("%c",&map[i][j]);
        //getchar();//读掉回车
    }
    bfs();
    return 0;
}